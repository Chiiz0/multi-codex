const dictionaries: Record<Language, Dictionary> = { en, zh };

type I18nValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, params?: TranslationParams) => string;
};

const I18nContext = createContext<I18nValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(readInitialLanguage);

  useEffect(() => {
    window.localStorage.setItem(languageKey, language);
    document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  }, [language]);

  const value = useMemo<I18nValue>(
    () => ({
      language,
      setLanguage: setLanguageState,
      t: (key, params) => formatMessage(dictionaries[language][key] ?? en[key] ?? key, params)
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return value;
}

export function LanguageToggle() {
  const { language, setLanguage, t } = useI18n();
  return (
    <div className="language-toggle" aria-label={t("language.label")}>
      <button
        aria-pressed={language === "en"}
        className={language === "en" ? "active" : ""}
        type="button"
        onClick={() => setLanguage("en")}
      >
        {t("language.en")}
      </button>
      <button
        aria-pressed={language === "zh"}
        className={language === "zh" ? "active" : ""}
        type="button"
        onClick={() => setLanguage("zh")}
      >
        {t("language.zh")}
      </button>
    </div>
  );
}

function readInitialLanguage(): Language {
  const stored = window.localStorage.getItem(languageKey);
  if (stored === "en" || stored === "zh") {
    return stored;
  }
  return window.navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function formatMessage(template: string, params?: TranslationParams) {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}
